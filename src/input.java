    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            // 1. Validate Placeholder Text
            String weightText = jTextField2.getText().trim();
            String heightText = jTextField3.getText().trim();
            
            if (weightText.equals("Enter your weight") || heightText.equals("Enter your height") || 
                weightText.isEmpty() || heightText.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter valid numeric values for Weight and Height.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // 2. Parse numbers safely
            double weight = Double.parseDouble(weightText);
            double height = Double.parseDouble(heightText);
            int age = (int) jSpinner1.getValue();
            
            if (weight <= 0 || height <= 0) {
                JOptionPane.showMessageDialog(this, "Weight and Height must be greater than zero.", "Validation Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // 3. Auto-convert height if entered in Centimeters (e.g., 170 instead of 1.7)
            if (height > 3.0) {
                height = height / 100.0;
            }
            
            // 4. Compute BMI formula
            double bmiValue = weight / (height * height);
            
            // 5. Determine status classifications
            String status;
            if (age < 18) {
                // Simplified growth chart warning logic for minors
                if (bmiValue < 15.0) status = "Underweight (Age Alert)";
                else if (bmiValue > 26.0) status = "Overweight (Age Alert)";
                else status = "Normal Range for Age";
            } else {
                // Standard adult World Health Organization classifications
                if (bmiValue < 18.5) status = "Underweight";
                else if (bmiValue < 25.0) status = "Normal weight";
                else if (bmiValue < 30.0) status = "Overweight";
                else status = "Obese";
            }
            
            // 6. Update UI Labels safely formatted to 1 decimal place
            BMI.setText(String.format("BMI: %.1f", bmiValue));
            BMIstatus.setText("BMI Status: " + status);
            
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please check your format. Use numbers only (e.g., Weight: 70, Height: 1.75).", "Format Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

