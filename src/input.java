    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
                                       

    String disease = "";
    if (chkDiabetes.isSelected())          disease = "Diabetes";
    else if (chkThyroid.isSelected())      disease = "Thyroid";
    else if (chkPCOS.isSelected())         disease = "PCOS";
    else if (chkCholestrol.isSelected())   disease = "Cholesterol";
    else if (chkHypertension.isSelected()) disease = "Hypertension";
    else if (chkFattyLiver.isSelected())   disease = "Fatty Liver";

    if (disease.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please select a disease first!");
        return;
    }

    StringBuilder sb = new StringBuilder();
    sb.append("╔══════════════════════════════════════╗\n");
    sb.append("         🥗 SMART DIET PLAN\n");
    sb.append("   Condition : ").append(disease).append("\n");
    sb.append("╚══════════════════════════════════════╝\n\n");

    String[] mealTypes = {"Breakfast", "Lunch", "Dinner", "Snacks"};
    boolean anyFound = false;

    for (String mealType : mealTypes) {
        List<String[]> meals = DietPlanReader.filter(disease, mealType);

        if (!meals.isEmpty()) {
            anyFound = true;
            sb.append("==========================================\n");
            sb.append("   🍽  ").append(mealType.toUpperCase()).append("\n");
            sb.append("==========================================\n");

            int count = 1;
            for (String[] m : meals) {
                sb.append(count++).append(". ").append(m[2]).append("\n");
                sb.append("   Ingredients : ").append(m[3]).append("\n");
                sb.append("------------------------------------------\n");
            }
            sb.append("\n");
        }
    }

    if (!anyFound) {
        JOptionPane.showMessageDialog(this, "No meals found!\nCheck your diet_plan.txt file.");
    } else {
        
        java.util.Map<String, java.util.Map<String, String>> weeklyPlan = new java.util.LinkedHashMap<>();
        String[] days = {"Monday","Tuesday","Wednesday","Thursday","Friday","Saturday","Sunday"};
        String[] mealTypesForInter = {"Breakfast","Lunch","Dinner","Snacks"};

    
        java.util.Random rand = new java.util.Random();

        for (String day : days) {
            java.util.Map<String, String> dayMeals = new java.util.LinkedHashMap<>();
            for (String mt : mealTypesForInter) {
                
    
                java.util.List<String[]> meals = DietPlanReader.filter(disease, mt);
                
                if (!meals.isEmpty()) {
               
                    java.util.List<String[]> modifiableMeals = new java.util.ArrayList<>(meals);
                    
        
                    java.util.Collections.shuffle(modifiableMeals);
                    
                    StringBuilder mealSb = new StringBuilder();
                    
            
                    int mealsToPick = Math.min(2, modifiableMeals.size());
                    for (int i = 0; i < mealsToPick; i++) {
                        String[] m = modifiableMeals.get(i);
                        mealSb.append(m[2]).append("\n");
                        mealSb.append("Ingredients: ").append(m[3]).append("\n\n");
                    }
                    dayMeals.put(mt, mealSb.toString().trim());
                } else {
                    dayMeals.put(mt, "No meal data available.");
                }
            }
            weeklyPlan.put(day, dayMeals);
        }

        ResultInter ri = new ResultInter();
        ri.populatePlan(weeklyPlan);
        ri.setVisible(true);
        this.dispose(); 
    
}
       
    }//GEN-LAST:event_jButton3ActionPerformed

